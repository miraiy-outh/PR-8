package com.company.z1;

abstract public class Shape {

    protected String color;
    protected int width;
    protected int length;
    protected int x;
    protected int y;

    public Shape(String color, int width, int length, int x, int y) {
        this.color = color;
        this.width = width;
        this.length = length;
        this.x = x;
        this.y = y;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}